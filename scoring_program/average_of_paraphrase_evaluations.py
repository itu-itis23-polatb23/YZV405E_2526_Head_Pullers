import argparse
import collections
import math
import os
import re
import sys
import json

##########################################

## Regular expressions to extract useful data from the stdout of evaluate.py
RE_BERT = re.compile(r'(f-bertscore)[ a-z\:]+([0-9\.]+)')
RE_DIV_CURRENT = re.compile(r'Entropy, variety, balance for the (current) system:\s*([0-9\.]+), ([0-9\.]+), ([0-9\.]+)')
RE_DIV_MIN = re.compile(r'Entropy, variety, balance for the (minimal) reference:\s*([0-9\.]+), ([0-9\.]+), ([0-9\.]+)')
RE_DIV_CREA = re.compile(r'Entropy, variety, balance for the (creative) reference:\s*([0-9\.]+), ([0-9\.]+), ([0-9\.]+)')

#Default name of the result file
RES_FILE = "results.txt"

def dir_path(string):
    if os.path.isdir(string):
        return string
    else:
        raise NotADirectoryError(string)

parser = argparse.ArgumentParser(description="""
        Calculate macro-average of paraphrasing scores from multiple evaluate.py output files.""")
parser.add_argument("nb_langs",  help="""Total number of languages in the competition""", type= int)
parser.add_argument("result_dir", help="""Directory of system results, with one folder per language""", type=dir_path)
parser.add_argument("out_json",  help="""The output json file for the average scores""", type=argparse.FileType('w'))
parser.add_argument("out_html",  help="""The output html file for the average scores""", type=argparse.FileType('w'))

#parser.add_argument("evaluation_files", nargs='+', type=argparse.FileType('r'),
#        help="""Files that were produced as output of evaluate.py""")


def format_float(value: float):
    r"""Return pretty-formatted float."""
    return "{:.2f}".format(value)


##########################################
class Main(object):
    def __init__(self, args):
        self.args = args

    def run(self):
    
        # Extract relevant data from all the input filres
        #results = self.parse_files(self.args.evaluation_files)
        print("Loading scores...")
        results = self.parse_files(self.args.result_dir)
        
        #Aggregate the bert-scores
        bert_ave = self.average_bert(self.args.nb_langs, results)
        all_results = {"Global-BERTScore": bert_ave, "All_scores": results}
        
        # Generate JSON with all scores for a system
        print("Generating .json file with average scores...")
        self.generate_json(all_results, self.args.out_json)
                
        # Generate HTML with all scores for a system
        print("Generating .html file with average scores...")
        self.generate_html(all_results, len(results), self.args.nb_langs, self.args.out_html)

##########################################
    def parse_files(self,result_dir) -> (int, dict):
        r"""Return the number of laguages covered by a system, and useful data from a directory of files generated by evaluate.py, with 1 folder per language code
        e.g. 
        [{'language': 'EN', 'scores': {'bertscore': 31.32, 'div_current_entropy': 1.099, 'div_current_variety': 3, 'div_current_balance': 1.0, \ 
          'div_min_entropy': 1.386, 'div_min_variety': 4, 'div_min_balance': 1.0, 'div_crea_entropy': 2.941, 'div_crea_variety': 20, 'div_crea_balance': 0.982}}, \
         {'language': 'FR', 'scores': {'bertscore': 100.0, 'div_current_entropy': 1.386, 'div_current_variety': 4, 'div_current_balance': 1.0, \
          'div_min_entropy': 0.693, 'div_min_variety': 2, 'div_min_balance': 1.0, 'div_crea_entropy': 2.773, 'div_crea_variety': 16, 'div_crea_balance': 1.0}}]
        """
        RESULTS = [] #Results from all files
        
        langs = os.listdir(result_dir)
        langs.sort()
        
        for lang in langs:
            
            res_file_path = os.path.join(result_dir, lang, RES_FILE)
            if os.path.isfile(res_file_path) and os.access(res_file_path, os.R_OK):
                res_file = open(res_file_path, 'r+')
                RES = {}
                RES["language"] = lang
                scores = {}
                lines = res_file.read().split("\n")
                for line in lines:
                    res=self.parse_line(line)
                    if res:
                        scores.update(res[0])
                RES["scores"] = scores 
                RESULTS.append(RES)  
            else:
                print("File", res_file_path, "not found. The scores for this language are considered 0.")
            
        
        return RESULTS
        
##########################################
    def parse_line(self,line: str) -> list:
        r"""Return useful data from a file generated by evaluate.py
        e.g. {'bertscore': 100.0}
             OR
             {'div_current_entropy': 1.386, 'div_current_variety': 4.0, 'div_current_balance': 1.0}
             OR
             {'div_min_entropy': 0.693, 'div_min_variety': 2.0, 'div_min_balance': 1.0}
             OR
             {'div_crea_entropy': 2.773, 'div_crea_variety': 16.0, 'div_crea_balance': 1.0}
        """
    
        bertscore = RE_BERT.findall(line) 
        if bertscore:
            #print(bertscore)
            bertscore_dict = {'bertscore': float(bertscore[0][1])}
            return [bertscore_dict]
            #print("BERTSCORE:",bertscore)
    
        div_current = RE_DIV_CURRENT.findall(line)
        if div_current:
            div_current_dict = {}
            div_current_dict['div_current_entropy'] = float(div_current[0][1])
            div_current_dict['div_current_variety'] = int(div_current[0][2])
            div_current_dict['div_current_balance'] = float(div_current[0][3])
            return [div_current_dict]
            #print("DIV_CURRENT:",div_current)
    
        div_min = RE_DIV_MIN.findall(line)
        if div_min:
            div_min_dict = {}
            div_min_dict['div_min_entropy'] = float(div_min[0][1])
            div_min_dict['div_min_variety'] = int(div_min[0][2])
            div_min_dict['div_min_balance'] = float(div_min[0][3])
            return [div_min_dict]
            #print("DIV_MIN:",div_min)
    
        div_crea = RE_DIV_CREA.findall(line)
        if div_crea:
            div_crea_dict = {}
            div_crea_dict['div_crea_entropy'] = float(div_crea[0][1])
            div_crea_dict['div_crea_variety'] = int(div_crea[0][2])
            div_crea_dict['div_crea_balance'] = float(div_crea[0][3])
            return [div_crea_dict]
            #print("DIV_CREA:",div_crea)
            
        return([])
        
##########################################
    def average_bert(self,nb_langs,results: dict):
       r"""From a list of results which looks like the reult of parse_files, aggregate all bert-scores into an average"""
       
       bert_aggr = float(0)
       for lang_res in results:
           bertscore = float(lang_res["scores"]["bertscore"])
           bert_aggr = bert_aggr + bertscore
       return(bert_aggr/nb_langs)
        
##########################################
    def generate_json(self, results, out_json):
        r'''Generate the scores.json file to the specified output_path. The results are of the form: 
           {'Global-BERT-score': 65.66, 
            'All_scores': {'language': 'EN', 'scores': {'bertscore': 31.32, 'div_current_entropy': 1.099, 'div_current_variety': 3, 'div_current_balance': 1.0, 
                           'div_min_entropy': 1.386, 'div_min_variety': 4, 'div_min_balance': 1.0, 'div_crea_entropy': 2.941, 'div_crea_variety': 20, 'div_crea_balance': 0.982}}, 
                          {'language': 'FR', 'scores': {'bertscore': 100.0, 'div_current_entropy': 1.386, 'div_current_variety': 4, 'div_current_balance': 1.0, 
                           'div_min_entropy': 0.693, 'div_min_variety': 2, 'div_min_balance': 1.0, 'div_crea_entropy': 2.773, 'div_crea_variety': 16, 'div_crea_balance': 1.0}}}
        '''
        
        out_json.write(json.dumps(results))

##########################################
    def generate_html(self, results, nb_langs_sys, nb_langs_total, out_html):
        r'''Generate the scores.json file to the specified output_path. 
            nb_langs_sys is the number of langauges covered by the system
            nb_langs_total is the total number of langauges in the competition
            The results are of the form: 
            {'Global-BERT-score': 65.66, 
             'All_scores': {'language': 'EN', 'scores': {'bertscore': 31.32, 'div_current_entropy': 1.099, 'div_current_variety': 3, 'div_current_balance': 1.0, 
                            'div_min_entropy': 1.386, 'div_min_variety': 4, 'div_min_balance': 1.0, 'div_crea_entropy': 2.941, 'div_crea_variety': 20, 'div_crea_balance': 0.982}}, 
                           {'language': 'FR', 'scores': {'bertscore': 100.0, 'div_current_entropy': 1.386, 'div_current_variety': 4, 'div_current_balance': 1.0, 
                            'div_min_entropy': 0.693, 'div_min_variety': 2, 'div_min_balance': 1.0, 'div_crea_entropy': 2.773, 'div_crea_variety': 16, 'div_crea_balance': 1.0}}}
        '''
        s = self._html(results, nb_langs_sys, nb_langs_total)
        out_html.write(s)

#####################################################
