# 
# 
# DIVERSITY :
# Entropy, variety, balance for the current system:        1.386, 4, 1.000
# Entropy, variety, balance for the minimal reference:     0.693, 2, 1.000
# Entropy, variety, balance for the creative reference:    2.773, 16, 1.000
