#-------------------------------------- /home/savary/PARSEME/PARSEME-corpora/Gitlab/sharedtask-data-dev/2.0/preliminary-sharedtask-data/subtask2_trial/FR/prediction.json --------------------------------------
# PERFORMANCE :
# Average f-bertscore for the current system: 100.00
# Number of evaluated elements: 2
