            '\tbackground-color: #34495e;\n'
            '\tborder: 1px solid #ffffff;\n'
            '\tcolor: #ffffff;\n'
            '\ttext-align: center;\n'
            '}\n'
            '.styled-table th,\n'
            '.styled-table td {\n'
            '\tpadding: 12px 15px;\n'
            '\ttext-align: center;\n'
